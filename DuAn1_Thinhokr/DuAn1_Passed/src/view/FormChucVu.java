    private void loadTable(ArrayList<ViewModeChucVu> listChucVu) {

        DefaultTableModel model = (DefaultTableModel) tblChucVu.getModel();
        model.setColumnCount(0);
        model.addColumn("Mã Chức Vụ");
        model.addColumn("Tên Chức Vụ");
        model.setRowCount(0);
        for (ViewModeChucVu x : listChucVu) {
            model.addRow(new Object[]{x.getMaCv(), x.getTenCv()});
        }
    }

