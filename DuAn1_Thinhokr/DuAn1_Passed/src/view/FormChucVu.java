    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (validateForm()) {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn thêm ?", "Xác nhân thêm.", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                if (chucVuSe.checkTrungMa(txtMa.getText().trim())) {
                    ChucVu cv = new ChucVu();
                    cv.setMaCv(txtMa.getText().trim());
                    cv.setTenCv(txtTen.getText().trim());
                    chucVuSe.them(cv);
                    JOptionPane.showMessageDialog(this, "Thêm Thành Công");
                    loadTable(chucVuSe.getAll());
                } else {
                    JOptionPane.showMessageDialog(this, "Trùng mã chức vụ");
                }
            } else {
                return;
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (validateForm()) {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn sửa ?", "Xác nhân sửa.", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                ChucVu cv = new ChucVu();
                cv.setMaCv(txtMa.getText().trim());
                cv.setTenCv(txtTen.getText().trim());
                int ketQua = chucVuSe.sua(cv, txtMa.getText().trim());
                if (ketQua < 0) {
                    JOptionPane.showMessageDialog(this, "Sửa Thất Bại");
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa Thành Công");
                    loadTable(chucVuSe.getAll());
                }
            } else {
                return;
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (txtMa.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Mời chọn mã chức vụ");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa ?", "Xác nhân xóa.", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            int ketQua = chucVuSe.delete(txtMa.getText().trim());
            if (ketQua < 0) {
                JOptionPane.showMessageDialog(this, "Xóa Thất Bại");
            } else {
                JOptionPane.showMessageDialog(this, "Xóa Thành Công");
                loadTable(chucVuSe.getAll());
                loadTable(chucVuSe.getAll1());
            }
        } else {
            return;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

